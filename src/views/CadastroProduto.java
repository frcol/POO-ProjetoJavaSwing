    private void atualizarCombo() {
        try {
            List<CategoriaProduto> lstCategoria = new CategoriaProdutoDAO().selecionarTodos();
            cmbCategoria.setModel(new DefaultComboBoxModel<CategoriaProduto>(lstCategoria.toArray(new CategoriaProduto[lstCategoria.size()])));
        } catch (Exception e) {
            
        }
    }
