    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        atualizarCombo();
        
        if(objProduto != null) {
            txtCodigo.setText(String.valueOf(objProduto.getId()));
            txtDesc.setText(objProduto.getDescricao());
            txtQtde.setText(objProduto.getQuantidade()+"");
            txtValor.setText(objProduto.getValorUnitatio()+"");
            cmbCategoria.setSelectedItem(objProduto.getCategoriaProduto());
        } 
    }//GEN-LAST:event_formWindowActivated

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (objProduto == null) {
            objProduto = new Produto();
        }
        
        objProduto.setDescricao(txtDesc.getText());
        objProduto.setQuantidade(Integer.parseInt(txtQtde.getText()));
        objProduto.setValorUnitatio(Double.parseDouble(txtValor.getText()));
        objProduto.setCategoriaProduto((CategoriaProduto)cmbCategoria.getSelectedItem());
        
        new ProdutoDAO().editar(objProduto);
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

